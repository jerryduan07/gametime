#!/usr/bin/env python

"""Exposes classes and functions to maintain a representation of,
and information about, the model produced by an SMT solver
in response to a satisfiable SMT query.
"""

"""See the LICENSE file, located in the root directory of
the source distribution and
at http://verifun.eecs.berkeley.edu/gametime/about/LICENSE,
for details on the GameTime license and authors.
"""


class Model(object):
    """Maintains a representation of, and information about,
    the model produced by an SMT solver in response to
    a satisfiable SMT query.

    Attributes:
        modelStr:
            String that represents the model generated by
            an SMT solver in response to a satisfiable SMT query.
    """

    def __init__(self, modelStr=""):
        self.modelStr = modelStr

    def __str__(self):
        return self.modelStr
